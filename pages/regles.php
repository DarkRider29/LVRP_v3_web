<h5> Principales règles :</h5><br />
Il est interdit de Bunny, Carkill, DM, Powergame, Metagame pour cela aller dans en haut à gauche > Aide > Définition.<br /><br />
                                       
<h5>Les Drive By :</h5><br />
<ul>
<li>Le Drive n'est autorisé qu'en passager et seulement passager avec une raison valable.Toute personne prise en flât grand délit sera punnie. (le staff a les yeux partout)</li>
<li>Armes autorisées : MP5 - Colt 45 - Silencieux - Uzi - Tec 9 </li>
</l><br /><br />
                     
<h5>CarJack :</h5><br />
Il est strictement interdit de volé le véhicule de quelqu'un sans l'utilisation de /me et /do, c'est à éviter en LP. Par exemple quelqu’un est à un stop, un homme toc à la vitre et lui parle, il utilisera comme /me : /me tente d’ouvrir d'un geste rapide et fort la porte de la voiture.
/do Reussi t-il ?<br />
Si le conducteur a réellement vérouiller le véhicule il fait : <br />
/do Non car la voiture est vérouiller. Si il vien de le verouiller ou il ne la pas verrouiller, cela est considéré comme mensonge OOC.<br />
 <br /><br />                    
<h5>Le stunt :</h5><br />
Le stunt est strinctement interdit dans Los Santos. Mais vous avez la possiblité d'en faire lors des events ou dans les stadiums. En cas de non respect de cette règle des sanctions s'y appliqueront. Nous vous invitons à lire la définition du stunt.
 <br /><br /><br />                      
<h5>Faireplay :</h5><br />
Soyez fairplay, ne vous avantagez pas toujours, sachez "perdre".<br /><br />
                 
Utilisation du /me et /do en scène :<br />
<br />Il est très conseiller d'utiliser les /me et /do en scène pour donner des détails sur vos actions. Si quelqu’un vous pose une question en /do, vous devez obligatoirement y répondre.
<br />Ses deux commandes sont complètements OOC, il ne doit donc pas y avoir de mensonges. Cela sera considéré comme mensonge OOC.
 <br /><br />                    
Conduite Rôleplay :<br />
<br />Que vous êtes dans une campagne, dans une autre ville déserte, la conduite doit rester roleplay. Arrêter vous au stop (Les feu rouges ne conte pas). Si vous roulez sans ralentirs au intersections n importe comment. Cela sera considéré comme conduite non-roleplay.
 <br />                   
<hr class="dotted" />
 <br />            
Tricherie :<br />
<br />Il est interdit d'utiliser des logiciels de tricherie, ou de tricher, sachez que si vous demander le logiciel, recevez de l'argent, demandez... sans nous prévenir et nous rendre celle-ci, vous devenez complice. Cela sera donc considéré comme complicité de tricherie. Le bannissement sera donc définitif.
<br /><br />                 
A.F.K :
<br />Il est autorisé d'être AFK durant quelque minutes pour diverse raison, un système d'anti AFK est mis en place pour éviter les abus, si vous abusez d'être AFK pour recevoir les Paydays par exemple, vous pourrez avoir des warn, voir être emprisonner.
 <br /><br />                              
Language SMS :<br />
<br /><br />Il est interdit d'utiliser le language SMS du style "stp", sauf en /t sms (pour envoyer un sms IG), N'oubliez pas qu'une phrase commence par une majuscule et finis par un point.
                 
Utilisation de bug :<br />
<br /><br />Il est strictement interdit d'utiliser des bugs du scriptage sur notre serveur ainsi que sur le forum.
                 
Les insultes :<br />
Les insultes InGame sont totalement tolérées, mais à l'inverse, les insulte OOC sont strictement interdites et passives d'une lourde sanction.<br />
  <br /><br />               
Les contacts avec les admins IG :<br />
Évitez de faire des rapport inutile, si vous savez que l'action non-roleplay est déjà passer, penser à screen au par avant, puis à poster sur le forum.<br />
Évitez les /i inutile, posez vos questions directement et évitez de faire plusieurs /i pour dire une chose comme ci-dessous :<br />
/i Vien.<br />
/i Tu peut te tp ?<br />
               
Langue :<br />
<br />Le serveur L.V.R.P est serveur francophone, la seule langue autorisé est le français, vous pouvez parler IG de votre langue si quelqu’un la connais, il existe une école de langue IG. Exemple si votre langue est l’allemand (vous jouer un roleplay allemand) /allemand On fait quoi ?
 <br /><br />                
Publicité pour d'autre serveur :<br />
Il est strictement interdit de faire de la publicité pour d'autre serveur sous peine de bannissement immédiat.<br />
<br />               
<hr class="dotted" />
<br />                     
Les comptes :<br />
Vous n'avez droit qu'à un seul et unique compte. Le double compte est interdit et est passible d'un ban définitif.<br />
Il vous est interdit de partager/vendre votre compte. Vous êtes seul responsable de votre compte, en cas de piratage de celui çi nous n'en sommes donc en aucuns cas responsable.<br />
                   
Changer de compte :<br />
Comme déja dit, c'est 1 compte par personne, il y'a une possibilité de rename grâce au pack VIP ou votre 1 point de rename tous les mois.<br />
<br /><br />                 
Plusieurs sur la même IP :<br />
<br />Si vous êtes plusieurs sur la même adresse Ip, il faudra prévenir le staff, (frères, soeurs...) Si quelqu’un est bannis, il pourra infecté tout les autres. Le compte sera donc verrouiller, puis bannis s'il doit être.
<br />                      
<hr class="dotted" />
 <br />                
Les lieux Publics (LP) :<br />
Les lieux publics sont les lieux fréquemment fréquenter par exemple: La station, la banque, la pizzeria etc.<br />
Un lieu ne devient plus public aux alentours de 23heures/minuit IG, Le poste de police, la marie, l'hopital et la caserne de pompiers, sont LP tout le temps.<br />
Il est strictement interdit de sortir une arme dans les lieux publics, peu importe la raison. De lourdes sanctions seront prises contre les personnes qui passeront outre cette règle.<br />
<br /><br />      
Intouchable :<br />
A partir du moment où vous êtes nouveau (lvl1) en ville (débutant) vous êtes compris "intouchable" , les nouveaux sur le serveur sont "intouchable", il est interdit de les braquer, kidnappé, ils "découvrent" le roleplay.
<br />En tant qu'intouchable vous n'avez pas le droit de disposez d'arme ou de drogue. Vous pouvez fréquenté des mafias / gangs sans y être dans leurs effectifs officiel. Biensur si un joueur vous vend des armes / drogues vous devez les refuser.
<br />Vous ne participez pas au brawl / fusillade.
<br /><br />               
Les Scènes :<br />
A partir du moment ou vous êtes en scène, vous n'aviez pas le droit de vous déconnecter sans rien dire (Il existe les urgences), tâchez de prévenir en OOC avant, favorisez le /chu  (qui permet de chuchoter) pour parler en OOC à une personne. Donc une déconnections  puis reconnections non justifier sera sévèrement sanctionner.
<br />Cette règles est aussi valable pour l’hôpital (Déconnexion pour éviter de payer l'hosto).
 <br /><br />            
Le OOC en scène :<br />
<br />Il est interdit d'OOC en scène car, cela peut être très gênant. Pour éviter le OOC en scène veuillez faire la commande ( /chu ) pour chuchoter au joueur concerné. Si vous ne respectez pas cette règle vous serrer sévèrement punie.
 <br />            
Les publicités / Rendez vous :
<br />Les publicités et Rendez vous se font totalement IG , il est interdit de parler / négocier de votre offre dans un chanel OOC. Pour les rendez-vous apeller la personne au téléphone. C'est interdit de le faire via un chat OOC.
 <br /><br />            
La prison :<br />
<br />Si vous êtes en prison IC, vous pourrez jouer une scène, si vous êtes en prison OOC, il est interdit de faire de scène. Pour l'utilisation du téléphone c'est complètement interdit en prison.
<br /><br />
           
Les Rackets & Kidnapping :<br />
Le montant maximum possible de racketter est de 10.000$, tout racket d'un montant supérieur se verra lourdement sanctionner.<br />
<br />Vous n'avez pas le droit de faire un racket de type vidage de compte en banque, inutile donc de kidnapper quelqu'un et d'aller à la banque ou dans un distributeur lui faire retirer de l'argent.  Il ne faudra pas se plaindre, si vous trouver des substances drogues / matériaux, vous n'aviez pas le droit de prendre plus de la moitié.
<br />Vous devez être minimum plus d'une personne pour tenter un kidnapping.
Il est interdit de kidnappé un journaliste pour éviter les abus, sauf si celui-ci traine dans le HQ / hood de votre faction.<br />
Attention, si vous prenez une victime dans votre véhicule, cela devient un kidnapping.<br />
Les rackets et Kidnapping sont bien évidemment interdits en lieux publics.<br />
           <br /><br />
Les permis :<br />
Les permis sont disponnible à l'autoécole, le permis de port d'arme est distribué par la LSPD, le tarifs général est de 10 000$, le prix peut varié en fonction de l'agent qui vous la fournis.<br />
     <br /><br />    
Le "droit" de tuer :<br />
Vous avez le droit de tuer à partir du moment que le joueur et vous êtes  niveau 3, vous devez avoir une raison valable, dans le cas contraire cela sera considéré comme du deathmatch.<br />
       <br /><br />
Tuez au poing :<br />
Lorsque vous tuez quelqu’un au poing, même avec une arme blanche, cela peut être du à une grosse bagarre, on ne meurt pas forcément lors d'une bagarre, donc penser à faire perdre la mémoire de votre victime en utilisant /me brise la nuke de "X" en mettant des coups de pied.<br />
       <br /><br />
Scène de sexe :<br />
Il est conseillez d’éviter les scènes de sexes pour la simple cause qu'il y'a des mineurs sur GTA San Andreas, vous pouvez en faire mais ceci est à éviter, si vous en abusez d'une manière, vous serai avertis, puis sanctionner.<br />
Les DDF Inutiles :<br />
Il est interdit de faire un Delit De Fuite (dit DDF) inutile sous peine de sanction immédiats, pour DDF il faut une bonne raison IC.<br />
<hr class="dotted" />
            <br />             
Les Factions :<br />
Il est possible de rejoindre une faction qu'a partir du niveau 3, vous pourrez avant les fréquenté sans y être dans l'effectif officiel. Si vous êtes un simple "habitant" du quartier ou quelqu’un qui fréquente la faction, vous aurez le droit de participez au fusillade / brawl.<br />
Sachez que si vous ne respecter pas les 3 jour civil, vous aurez un baissement de 3 jour pour les finirs (même si vous avez fait 1 jour).<br />
     <br /><br />                
Levels inférieurs à 3 :<br />
Toutes les personnes de niveau inférieur à 3 sont en phrase de visite et découverte au sein du serveur, elles sont donc intouchables vous n'avez donc en aucun cas et peu importe les raisons, pas le droit de les tués ou racketter.<br />
                             <br /><br />
L'aide faction est strictement interdite et passible de lourdes sanctions :<br />
Quand vous quittez une faction (ou êtes viré) vous êtes dans l'obligation d'effectuer 3 jours de civil. (vous pouvez faire une approche pendant ces trois jours).<br />
    <br />                         
Provocation envers les factions gouvernementale : (LSPD, Maire...)<br />
Il est autorisé de provoquer les factions gouvernementale, mais à un certain degré  si vous venez les provoquer en leurs disant des phrases, s'embrouiller normalement, cela sera autorisé, si vous passer devant la LSPD en moto plusieurs fois sans casque, puis leurs crie dessus des insultes, cela sera considéré comme abusive. Vous chercher donc l'action.<br />
             <br /><br />                        
Changer de faction :<br />
Vous êtes dans une faction ? Vous la quitté pour divers raison, il est obligatoire d'appliqué 3 jour civil. Si la faction est légale, par exemple passé de la LSPD au FBI (avec une scène), il y a pas besoin de faire 3 jour civil. Si vous passer d'une faction légal à illégal , vous pouvez aller sans faire de jour civil.<br />
     <br /><br />                          
Les gangs :<br />
Les membres d'un gang se doivent d'avoir une attitude corresponde à son Roleplay, c'est à dire, il est strictement interdit pour un gangster d'avoir en sa possession un véhicule de type luxe ( turismo, super gt, infernus..).<br />
Les gangs n'ont pas le droit de braquer un bizz en même temps qu'un autre se fait braquer.<br />
Un membre de gang n'a pas le droit de se rendre dans un QG de gang ennemie pour le provoquer.<br />
Les descentes ne doivent pas être excessives.<br />
                    <br /><br /> 
Les mafias :<br />
Les mafias se doivent d'être très discrètes sur leurs activités, pour cela vous devez prendre une couverture légale par exemple un petit restaurant italien pour une mafia italienne ou encore un salon de massage etc.<br />
Il est strictement interdit de vendre des armes de type M4 aux gangs.<br />
Les croisades avec la police doivent se faire minime, plus vous êtes loin d'eux, mieu vous vous portez.<br />
                   <br /><br />
Brawls :<br />
<br />Les brawls ont déjà était évoqué dans le paragraphe des factions, je rappelle qu'il faut avoir une raison pour faire un brawl (initiation, provocation...) Pour commencer un brawl, il faut un /me obligatoire et un réponse en /do, il ne doit y avoir que des armes contondante (batte, club, ect). Il est interdit d'avoir des armes à feu ou arme blanche (couteau,katana,ect..). Si la faction en face riposte avec des armes à feu ou arme blanche, ils seront sanctionné.
 <br />                       
<hr class="dotted" />
  <br />                     
Vente de véhicule :<br />
Vous pouvez vendre vos véhicules achetées au Concessionaire qui lui appartient (Lowride, 4x4, Vélo, Bateau, Luxe...) Dans le cas contraire, vous pourrez être sanctionné. La limite d'achat et de revente de véhicule entre joueur est fixé à 500 000$ maximume.<br />
Il y'a une exeption pour les véhicules coutent plus de 500 000, il est interdit de les vendres à un joueurs au dessus des 500 000, la règles reste la même on achète pas un hélicoptère pour en faire du buisness.<br />
         <br />                            
Braquage :<br />
Les braquages sont autorisé biensur avec des conditions.<br />
Pour braquer la banque il faut : 6 Policier en service.<br />
Pour braquer un petit commerce (Ex: Pizzeria) : 4 Policier en service.<br />
Braquage de banque : 1 tous les 3 jours pour les mafias. Les factions qui braquent changent à tour de rôle, se ne sera jamais la même faction qui braquera deux fois.  (6 FLICS en servies)<br />
Braquage de biz : 1 par jour. Une heure d'intervalle minimum <br />
                         <br /><br />
Le Staff se réserve le droit d'ajouter ou de modifier des règles à tout moment, si c'est le cas vous en serez informer.<br />
 
